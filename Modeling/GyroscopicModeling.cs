using System;
using System.Collections.Generic;

class GyroscopicModeling
{
    static void Main(string[] args)
    {
        // Данные задачи
        var data = new List<(int номер, double масса, double радиус, double амплитуда, double период)>
        {
            (1, 1000, 0.5, 10, 11),
            (2, 2000, 0.4, 9, 12),
            (3, 3000, 0.3, 8, 13),
            (4, 4000, 0.2, 7, 14),
            (5, 5000, 0.1, 8, 15),
            (6, 1000, 0.5, 9, 11),
            (7, 2000, 0.4, 9, 13),
            (8, 3000, 0.3, 8, 12),
            (9, 4000, 0.2, 7, 12),
            (10, 5000, 0.1, 7, 14),
            (11, 1000, 0.5, 8, 13),
            (12, 2000, 0.4, 9, 14),
            (13, 3000, 0.3, 8, 12),
            (14, 4000, 0.2, 8, 13),
            (15, 5000, 0.1, 7, 14),
            (16, 1000, 0.5, 9, 14),
            (17, 2000, 0.4, 9, 14),
            (18, 3000, 0.3, 8, 13),
            (19, 4000, 0.2, 8, 12),
            (20, 5000, 0.1, 9, 14),
            (21, 1000, 0.5, 10, 12),
            (22, 2000, 0.4, 8, 13),
            (23, 3000, 0.3, 8, 11),
            (24, 4000, 0.2, 9, 12),
            (25, 5000, 0.1, 10, 11),
        };

        // Константы
        double n = 3000; // скорость вращения ротора (об/мин)
        double L = 5;    // расстояние между подшипниками (м)
        double omega = 2 * Math.PI * n / 60; // угловая скорость вращения ротора

        // Результаты расчета
        double maxPressure = 0;
        int maxIndex = -1;

        // Заголовок таблицы
        Console.WriteLine($"{"№",-3} {"M (кг)",-8} {"R (м)",-6} {"I (кг·м²)",-10} {"Ω (рад/с)",-10} {"M_g (Н·м)",-12} {"P (Н/м²)",-10}");
        Console.WriteLine(new string('-', 60));

        foreach (var (номер, масса, радиус, амплитуда, период) in data)
        {
            // Момент инерции ротора
            double I = 0.5 * масса * Math.Pow(радиус, 2);

            // Угловая скорость прецессии
            double Omega = (2 * Math.PI / период) * Math.Sin(амплитуда * Math.PI / 180);

            // Гироскопический момент
            double M_g = I * omega * Omega;

            // Давление на подшипники
            double P = M_g / L;

            // Обновление максимального давления
            if (P > maxPressure)
            {
                maxPressure = P;
                maxIndex = номер;
            }

            // Вывод результатов
            Console.WriteLine($"{номер,-3} {масса,-8:F0} {радиус,-6:F2} {I,-10:F2} {Omega,-10:F2} {M_g,-12:F2} {P,-10:F2}");
        }

        // Вывод максимального давления
        Console.WriteLine($"\nМаксимальное давление: {maxPressure:F2} Н/м² (№ {maxIndex})");
    }
}